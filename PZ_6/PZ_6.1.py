"""
Дан целочисленный список размера 10.
Вывести все содержащиеся в данном списке чётные числа
в порядке убывания их индексов, а также их количество K.
"""
def task1():
        numbers = [4, 7, 2, 9, 8, 5, 1, 6, 3, 10]
        print("Исходный список:", numbers)
        print("Чётные числа в порядке убывания индексов:")
        k = 0
        for i in range(len(numbers) - 1, -1, -1):
            try:
                if numbers[i] % 2 == 0:
                    print(f"Индекс {i}: число {numbers[i]}")
                    k += 1
            except TypeError as e:
                print(f"Ошибка в элементе с индексом {i}: {e}")
                continue
        print(f"Количество чётных чисел K = {k}")
task1()